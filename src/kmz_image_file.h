#ifndef kmz_image_file_h
#define kmz_image_file_h

/**
 * Defines the methods available to all kempozer image file types. This may be used by external libraries to provide new file loaders.
 */
struct kmz_image_file_type_t {
    /**
     * Allocates an uninitialized image file of this type.
     */
    void * const (* const _new)(void);

    void (* const _ctor)(void * const restrict, const void * const restrict);
};
typedef struct kmz_image_file_type_t KmzImageTypeFile;

#endif /* kmz_image_file_h */
